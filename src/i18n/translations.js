const patternTranslations = [
  {
    pattern: /^Willkommen im Dashboard als (.+)\.$/,
    translate: (match, lang) => `${translations[lang]['Willkommen im Dashboard als'] ?? match[0]} ${roleLabels[lang][match[1]] ?? match[1]}.`,
  },
  {
    pattern: /^(\d+)\s+neu$/,
    translate: (match, lang) => {
      const suffixByLanguage = { de: 'neu', en: 'new', it: 'nuovi', fr: 'nouveaux' }
      return `${match[1]} ${suffixByLanguage[lang] ?? match[2]}`
    },
  },
  {
    pattern: /^(\d+)\s+aktiv$/,
    translate: (match, lang) => {
      const suffixByLanguage = { de: 'aktiv', en: 'active', it: 'attivi', fr: 'actifs' }
      return `${match[1]} ${suffixByLanguage[lang] ?? match[2]}`
    },
  },
  {
    pattern: /^(\d+)\s+veröffentlichte Aufträge in\s+(\d{4})$/,
    translate: (match, lang) => {
      const templates = {
        de: `${match[1]} veröffentlichte Aufträge in ${match[2]}`,
        en: `${match[1]} published orders in ${match[2]}`,
        it: `${match[1]} ordini pubblicati nel ${match[2]}`,
        fr: `${match[1]} commandes publiées en ${match[2]}`,
      }
      return templates[lang] ?? match[0]
    },
  },
  {
    pattern: /^Stärkster Monat:\s+(.+)\s+\((\d+)\)$/,
    translate: (match, lang) => {
      const month = translateText(match[1], lang)
      const labels = {
        de: 'Stärkster Monat',
        en: 'Strongest Month',
        it: 'Mese più forte',
        fr: 'Mois le plus fort',
      }
      return `${labels[lang] ?? match[0]}: ${month} (${match[2]})`
    },
  },
  {
    pattern: /^Für\s+(\d{4})\s+wurden noch keine Veröffentlichungen gefunden\.$/,
    translate: (match, lang) => {
      const templates = {
        de: `Für ${match[1]} wurden noch keine Veröffentlichungen gefunden.`,
        en: `No publications were found for ${match[1]} yet.`,
        it: `Per il ${match[1]} non sono ancora state trovate pubblicazioni.`,
        fr: `Aucune publication n’a encore été trouvée pour ${match[1]}.`,
      }
      return templates[lang] ?? match[0]
    },
  },
]

export function translateText(input, language = 'de') {
  if (!input || language === 'de') {
    return input
  }

  const exactTranslation = translations[language]?.[input]

  if (exactTranslation) {
    return exactTranslation
  }

  for (const rule of patternTranslations) {
    const match = input.match(rule.pattern)

    if (match) {
      return rule.translate(match, language)
    }
  }

  return input
}
