const patternTranslations = [
  {
    pattern: /^Willkommen im Dashboard als (.+)\.$/,
    translate: (match, lang) => `${translations[lang]['Willkommen im Dashboard als'] ?? match[0]} ${roleLabels[lang][match[1]] ?? match[1]}.`,
  },
  {
    pattern: /^(\d+)\s+neu$/,
    translate: (match, lang) => {
      const suffixByLanguage = { de: 'neu', en: 'new', it: 'nuovi', fr: 'nouveaux' }
      return `${match[1]} ${suffixByLanguage[lang] ?? match[2]}`
    },
  },
  {
    pattern: /^(\d+)\s+aktiv$/,
    translate: (match, lang) => {
      const suffixByLanguage = { de: 'aktiv', en: 'active', it: 'attivi', fr: 'actifs' }
      return `${match[1]} ${suffixByLanguage[lang] ?? match[2]}`
    },
  },
]

export function translateText(input, language = 'de') {
  if (!input || language === 'de') {
    return input
  }

  const exactTranslation = translations[language]?.[input]

  if (exactTranslation) {
    return exactTranslation
  }

  for (const rule of patternTranslations) {
    const match = input.match(rule.pattern)

    if (match) {
      return rule.translate(match, language)
    }
  }

  return input
}
